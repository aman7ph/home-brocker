import mongoose from "mongoose";
import { userInterface } from "../types";

const userSchema = new mongoose.Schema<userInterface>(
  {
    username: {
      type: String,
      required: true,
      unique: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
    },
    password: {
      type: String,
      required: true,
    },
    phoneNumber: {
      type: String,
      required: true,
    },
    avatar: {
      type: String,
      required: true,
    },
  },
  { timestamps: true }
);

const User = mongoose.model<userInterface>("User", userSchema);

export default User;
